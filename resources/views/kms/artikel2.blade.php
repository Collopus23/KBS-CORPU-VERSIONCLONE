                                            <h2 class="post-title mt-6">Rencana Suksesi</h2>
                                            <div class="level2">

                                                <p>
                                                    Rencana Suksesi adalah perencanaan sistematis melalui pemetaan Suksesor
                                                    yang diproyeksikan dalam Jabatan Target.
                                                </p>
                                                <ul>
                                                    <li class="level1">
                                                        <div class="li">Kelompok Rencana Suksesi Instansi adalah
                                                            kelompok talenta pada masing-masing Instansi Pemerintah yang
                                                            berasal dari kotak 9 (sembilan), 8 (delapan), dan 7 (tujuh) yang
                                                            disiapkan untuk menduduki jabatan target di lingkungan
                                                            instansinya. </div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li">Kelompok Rencana Suksesi Nasional adalah
                                                            kelompok talenta yang berasal dari kotak 9 (sembilan) pada
                                                            masing-masing Instansi Pemerintah yang dihimpun oleh Tim
                                                            Manajemen Talenta ASN Nasional untuk disiapkan menduduki jabatan
                                                            target dalam lingkup nasional.</div>
                                                    </li>
                                                </ul>

