                                            <p>
                                                Aparatur Sipil Negara (ASN) menurut Pasal 1 <a
                                                    href="/ensiklopedia:uu05-2014" class="wikilink1"
                                                    title="ensiklopedia:uu05-2014" data-wiki-id="ensiklopedia:uu05-2014">UU
                                                    Nomor 5 Tahun 2014</a> adalah profesi bagi <a href="/ensiklopedia:pns"
                                                    class="wikilink1" title="ensiklopedia:pns"
                                                    data-wiki-id="ensiklopedia:pns">Pegawai Negeri Sipil</a> dan <a
                                                    href="/ensiklopedia:pppk" class="wikilink2" title="ensiklopedia:pppk"
                                                    rel="nofollow" data-wiki-id="ensiklopedia:pppk">Pegawai Pemerintah
                                                    dengan Perjanjian Kerja</a> yang bekerja pada <a
                                                    href="/ensiklopedia:instansi_pemerintah" class="wikilink2"
                                                    title="ensiklopedia:instansi_pemerintah" rel="nofollow"
                                                    data-wiki-id="ensiklopedia:instansi_pemerintah">Instansi Pemerintah</a>.
                                            </p>

                                            <p>
                                                Pegawai ASN adalah <a href="/ensiklopedia:pns" class="wikilink1"
                                                    title="ensiklopedia:pns" data-wiki-id="ensiklopedia:pns">PNS</a> dan
                                                PPPK yang diangkat oleh <a href="/ensiklopedia:pejabat_pembina_kepegawaian"
                                                    class="wikilink1" title="ensiklopedia:pejabat_pembina_kepegawaian"
                                                    data-wiki-id="ensiklopedia:pejabat_pembina_kepegawaian">Pejabat Pembina
                                                    Kepegawaian</a> dan diserahi tugas dalam suatu <a
                                                    href="/ensiklopedia:jabatan" class="wikilink1"
                                                    title="ensiklopedia:jabatan"
                                                    data-wiki-id="ensiklopedia:jabatan">jabatan</a> pemerintahan atau
                                                diserahi tugas negara lainnya dan digaji berdasarkan peraturan
                                                perundang-undangan.
                                            </p>

                                            <p>
                                                ASN dikelola menggunakan prinsip pengelolaan Sumber Daya Manusia dalam satu
                                                kerangka yang disebut dengan <a href="/ensiklopedia:manajemen_asn"
                                                    class="wikilink1" title="ensiklopedia:manajemen_asn"
                                                    data-wiki-id="ensiklopedia:manajemen_asn">Manajemen ASN</a> untuk
                                                menghasilkan Pegawai ASN yang profesional, memiliki nilai dasar, etika
                                                profesi, bebas dari intervensi politik, bersih dan praktik korupsi, kolusi
                                                dan nepotisme. Untuk mengelola data dan informasi terkait ASN, maka dibentuk
                                                suatu <a href="/ensiklopedia:sistem_informasi_asn" class="wikilink2"
                                                    title="ensiklopedia:sistem_informasi_asn" rel="nofollow"
                                                    data-wiki-id="ensiklopedia:sistem_informasi_asn">Sistem Informasi
                                                    ASN</a> yang disusun secara sistematis, menyeluruh dan terintegrasi
                                                dengan berbasis teknologi.
                                            </p>

                                            <h5 id="landasan_profesi_dan_kode_etik_asn">Landasan Profesi dan Kode Etik ASN
                                            </h5>
                                            <div class="level5">

                                                <p>
                                                    Menurut Pasal 3 Undang Undang Nomor 5 Tahun 2014, ASN sebagai profesi
                                                    berlandaskan pada prinsip sebagai berikut:
                                                </p>
                                                <ul>
                                                    <li class="level1">
                                                        <div class="li"> a. nilai dasar;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> b. kode etik dan kode perilaku</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> c. komitmen, integritas moral, dan tanggung
                                                            jawab pada pelayanan publik;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> d. kompetensi yang diperlukan sesuai dengan
                                                            bidang tugas;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> e. kualifikasi akademik;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> f. jaminan perlindungan hukum dalam
                                                            melaksanakan tugas; dan</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> g. profesionalitas jabatan.</div>
                                                    </li>
                                                </ul>

                                                <p>
                                                    Kemudian pada Pasal 4, nilai dasar yang dimaksud dalam Pasal 3 diperinci
                                                    sebagai berikut:
                                                </p>
                                                <ul>
                                                    <li class="level1">
                                                        <div class="li"> a. memegang teguh ideologi <a
                                                                href="/ensiklopedia:pancasila" class="wikilink2"
                                                                title="ensiklopedia:pancasila" rel="nofollow"
                                                                data-wiki-id="ensiklopedia:pancasila">Pancasila</a>;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> b. setia dan mempertahankan <a
                                                                href="/ensiklopedia:uud1945" class="wikilink2"
                                                                title="ensiklopedia:uud1945" rel="nofollow"
                                                                data-wiki-id="ensiklopedia:uud1945">Undang Undang Dasar
                                                                Negara Republik Indonesia Tahun 1945</a> serta pemerintahan
                                                            yang sah;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> c. mengabdi kepada negara dan rakyat
                                                            Indonesia;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> d. menjalankan tugas secara profesional dan
                                                            tidak berpihak;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> e. membuat keputusan berdasarkan prinsip
                                                            keahlian;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> f. menciptakan lingkungan kerja yang
                                                            nondiskriminatif;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> g. memelihara dan menjunjung tinggi standar
                                                            etika yang luhur;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> h. mempertanggungjawabkan tindakan dan
                                                            kinerjanya kepada publik;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> i. memiliki kemampuan dalam melaksanakan
                                                            kebijakan dan program pemerintah;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> j. memberikan layanan kepada publik secara
                                                            jujur, tanggap, cepat, tepat, akurat, berdaya guna, berhasil
                                                            guna, dan santun;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> k. mengutamakan kepemimpinan berkualitas
                                                            tinggi;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> l. menghargai komunikasi, konsultasi, dan
                                                            kerja sama;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> m. mengutamakan pencapaian hasil dan mendorong
                                                            kinerja pegawai;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> n. mendorong kesetaraan dalam pekerjaan; dan
                                                        </div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> o. meningkatkan efektivitas sistem
                                                            pemerintahan yang demokratis sebagai perangkat sistem karier.
                                                        </div>
                                                    </li>
                                                </ul>

                                                <p>
                                                    Selanjutnya pada Pasal 5, Kode Etik yang dimaksud pada Pasal 3 huruf b
                                                    bertujuan untuk menjaga martabat dan kehormatan ASN. Kode Etik dan Kode
                                                    PErialku tersebut berisi pengaturan periaku agar Pegawai ASN:
                                                </p>
                                                <ul>
                                                    <li class="level1">
                                                        <div class="li"> a. melaksanakan tugasnya dengan jujur,
                                                            bertanggung jawab, dan berintegritas tinggi;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> b. melaksanakan tugasnya dengan cermat dan
                                                            disiplin;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> c. melayani dengan sikap hormat, sopan, dan
                                                            tanpa tekanan;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> d. melaksanakan tugasnya sesuai dengan
                                                            ketentuan peraturan perundang-undangan;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> e. melaksanakan tugasnya sesuai dengan
                                                            perintah atasan atau Pejabat yang Berwenang sejauh tidak
                                                            bertentangan dengan ketentuan peraturan perundang-undangan dan
                                                        </div>
                                                    </li>
                                                </ul>

                                                <p>
                                                    etika pemerintahan;
                                                </p>
                                                <ul>
                                                    <li class="level1">
                                                        <div class="li"> f. menjaga kerahasiaan yang menyangkut
                                                            kebijakannegara;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> g. menggunakan kekayaan dan barang milik
                                                            negara secara bertanggung jawab, efektif, dan efisien;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> h. menjaga agar tidak terjadi konflik
                                                            kepentingan dalam melaksanakan tugasnya;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> i. memberikan informasi secara benar dan tidak
                                                            menyesatkan kepada pihak lain yang memerlukan informasi terkait
                                                            kepentingan kedinasan;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> j. tidak menyalahgunakan informasi intern
                                                            negara, tugas, status, kekuasaan, dan jabatannya untuk mendapat
                                                            atau mencari keuntungan atau manfaat bagi diri sendiri atau
                                                            untuk orang lain;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> k. memegang teguh nilai dasar ASN dan selalu
                                                            menjaga reputasi dan integritas ASN; dan</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> l. melaksanakan ketentuan peraturan perundang
                                                            undangan mengenai disiplin Pegawai ASN</div>
                                                    </li>
                                                </ul>

                                            </div>

                                            <h5 id="kedudukan_fungsi_tugas_dan_peran_asn">Kedudukan, Fungsi, Tugas dan
                                                Peran ASN</h5>
                                            <div class="level5">

                                                <p>
                                                    Menurut Pasal 8 dan Pasal 9 Undang Undang Nomor 5 Tahun 2014, Pegawai
                                                    ASN berkedudukan sebagai unsur aparatur negara. Pegawai ASN melaksanakan
                                                    kebijakan yang ditetapkan oleh pimpinan <a
                                                        href="/ensiklopedia:instansi_pemerintah" class="wikilink2"
                                                        title="ensiklopedia:instansi_pemerintah" rel="nofollow"
                                                        data-wiki-id="ensiklopedia:instansi_pemerintah">Instansi
                                                        Pemerintah</a> serta harus bebas dari pengaruh dan intervensi semua
                                                    golongan dan partai politik.
                                                </p>

                                                <p>
                                                    Fungsi Pegawai ASN dalam Pasal 10 didefinisikan sebagai:
                                                </p>
                                                <ul>
                                                    <li class="level1">
                                                        <div class="li"> pelaksana kebijakan publik;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> pelayan publik; dan</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> perekat dan pemersatu bangsa</div>
                                                    </li>
                                                </ul>

                                                <p>
                                                    Sedangkan tugas ASN pada Pasal 11 adalah:
                                                </p>
                                                <ul>
                                                    <li class="level1">
                                                        <div class="li"> melaksanakan kebijakan publik yang dibuat oleh
                                                            <a href="/ensiklopedia:pejabat_pembina_kepegawaian"
                                                                class="wikilink1"
                                                                title="ensiklopedia:pejabat_pembina_kepegawaian"
                                                                data-wiki-id="ensiklopedia:pejabat_pembina_kepegawaian">Pejabat
                                                                Pembina Kepegawaian</a> sesuai dengan ketentuan peraturan
                                                            perundang-undangan;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> memberikan <a
                                                                href="/ensiklopedia:pelayanan_publik" class="wikilink2"
                                                                title="ensiklopedia:pelayanan_publik" rel="nofollow"
                                                                data-wiki-id="ensiklopedia:pelayanan_publik">Pelayanan
                                                                Publik</a> yang profesional dan berkualitas; dan</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> mempererat persatuan dan kesatuan Negara
                                                            Kesatuan Republik Indonesia.</div>
                                                    </li>
                                                </ul>

                                                <p>
                                                    Peran ASN pada Pasal 12 adalah sebagai perencana, pelaksana, dan
                                                    pengawas penyelenggaraan tugas umum pemerintahan dan pembangunan
                                                    nasional melalui pelaksanaan kebijakan dan pelayanan publik yang
                                                    profesional, bebas dari intervensi politik, serta bersih dari praktik
                                                    korupsi, kolusi, dan nepotisme
                                                </p>

