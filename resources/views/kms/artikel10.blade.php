                                            <h4 id="tata_cara_penyampaian_usul_kebutuhan">Tata Cara Penyampaian Usul
                                                Kebutuhan</h4>
                                            <div class="level4">

                                                <p>
                                                    Setiap Instansi Pemerintah wajib menyusun informasi jabatan dan
                                                    menghitung jumlah kebutuhan pegawai ASN menggunakan sistem informasi
                                                    sesuai dengan ketentuan peraturan perundang-undangan.
                                                </p>

                                                <p>
                                                    Data jumlah pegawai ASN yang ada dirinci sesuai dengan jabatan dan
                                                    jenjangnya masing-masing pada saat penyusunan informasi jabatan.
                                                    Data jumlah <a href="/ensiklopedia:pns" class="wikilink1"
                                                        title="ensiklopedia:pns" data-wiki-id="ensiklopedia:pns">PNS</a>
                                                    yang mencapai batas usia pensiun dihitung untuk jangka waktu 5 (lima)
                                                    tahun yang akan datang.
                                                </p>

                                                <p>
                                                    Jumlah PPPK yang akan berakhir masa hubungan perjanjian kerja dihitung
                                                    untuk jangka waktu 5 (lima) tahun yang akan datang.
                                                    Jumlah pegawai ASN yang ada dibandingkan dengan jumlah kebutuhan pegawai
                                                    ASN kemudian dikurangi atau ditambahkan dengan <a
                                                        href="/ensiklopedia:pns" class="wikilink1"
                                                        title="ensiklopedia:pns" data-wiki-id="ensiklopedia:pns">PNS</a>
                                                    yang mencapai batas usia pensiun dan PPPK yang akan berakhir masa
                                                    hubungan perjanjian kerja dengan ketentuan sebagai berikut:
                                                </p>
                                                <ul>
                                                    <li class="level1">
                                                        <div class="li"> jika perbandingan antara jumlah pegawai ASN
                                                            yang ada dengan jumlah kebutuhan pegawai ASN hasilnya yaitu
                                                            kelebihan pegawai ASN, kelebihan tersebut dikurangi dengan <a
                                                                href="/ensiklopedia:pns" class="wikilink1"
                                                                title="ensiklopedia:pns"
                                                                data-wiki-id="ensiklopedia:pns">PNS</a> yang akan mencapai
                                                            batas usia pensiun dan PPPK yang akan berakhir masa hubungan
                                                            perjanjian kerja.</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> jika perbandingan antara jumlah pegawai ASN
                                                            yang ada dengan jumlah kebutuhan pegawai ASN hasilnya yaitu
                                                            kekurangan pegawai ASN, kekurangan tersebut ditambah dengan <a
                                                                href="/ensiklopedia:pns" class="wikilink1"
                                                                title="ensiklopedia:pns"
                                                                data-wiki-id="ensiklopedia:pns">PNS</a> yang akan mencapai
                                                            batas usia pensiun dan PPPK yang akan berakhir masa hubungan
                                                            perjanjian kerja.</div>
                                                    </li>
                                                </ul>

                                                <p>
                                                    Untuk Jabatan yang kekurangan jumlah pegawai ASN selanjutnya ditetapkan
                                                    sebagai prioritas untuk dipenuhi kebutuhannya dalam bentuk usul tambahan
                                                    kebutuhan tahun yang akan datang.
                                                </p>

                                                <p>
                                                    Dalam menetapkan prioritas, Instansi Pemerintah harus memperhatikan
                                                    tujuan dan rencana strategis masing-masing.
                                                </p>

                                                <p>
                                                    Hasil penghitungan kebutuhan jumlah Pegawai ASN, data jumlah Pegawai ASN
                                                    yang ada, data <a href="/ensiklopedia:pns" class="wikilink1"
                                                        title="ensiklopedia:pns" data-wiki-id="ensiklopedia:pns">PNS</a>
                                                    yang akan mencapai batas usia pensiun dan data PPPK yang akan berakhir
                                                    masa hubungan perjanjian kerja, hasil perbandingan antara data-data
                                                    tersebut, dan usul tambahan kebutuhan dituangkan ke dalam aplikasi yang
                                                    bersifat elektronik.
                                                </p>

                                                <p>
                                                    Dokumen usul tambahan kebutuhan yang telah disahkan oleh PPK
                                                    masing-masing Instansi Pemerintah wajib disampaikan kepada Menteri dan
                                                    Kepala BKN dengan melampirkan rencana strategis Instansi Pemerintah.
                                                </p>

