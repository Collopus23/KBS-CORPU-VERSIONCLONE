                                            <h3 class="sectionedit6" id="penyusunan_analisis_beban_kerja_abk">Penyusunan
                                                Analisis Beban Kerja (ABK)</h3>
                                            <div class="level3">

                                                <p>
                                                    <strong>Analisis Beban Kerja</strong> adalah teknik manajemen yang
                                                    dilakukan secara sistematis untuk memperoleh informasi mengenai tingkat
                                                    efektivitas dan efisiensi kerja organisasi berdasarkan volume kerja.
                                                </p>

                                                <p>
                                                    Analisis Beban Kerja digunakan untuk menghitung jumlah kebutuhan pegawai
                                                    atau Pemangku Jabatan berdasarkan sejumlah target pekerjaan yang harus
                                                    diselesaikan pada satuan waktu tertentu.
                                                </p>

                                                <p>
                                                    Dalam menghitung kebutuhan, banyak metode yang dapat dipergunakan. Namun
                                                    demikian, dalam pedoman ini disajikan metoda yang sederhana yang
                                                    memungkinkan dapat memberi kemudahan bagi instansi menggunakannya.
                                                </p>

                                                <p>
                                                    Metoda perhitungan kebutuhan pegawai yang dipilih adalah metode beban
                                                    kerja yang diidentifikasi dari :
                                                </p>
                                                <ul>
                                                    <li class="level1">
                                                        <div class="li"> Hasil kerja;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> Objek kerja;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> Peralatan kerja; dan</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> Tugas per tugas jabatan.</div>
                                                    </li>
                                                </ul>

                                                <p>
                                                    Hal di atas diuraikan sebagai berikut:
                                                </p>
                                                <ul>
                                                    <li class="level1">
                                                        <div class="li"> <strong>Pendekatan Hasil Kerja</strong></div>
                                                    </li>
                                                </ul>

                                                <p>
                                                    Hasil kerja adalah produk atau output jabatan. Metode dengan pendekatan
                                                    hasil kerja adalah menghitung kebutuhan dengan mengidentifikasi beban
                                                    kerja dari hasil kerja jabatan. Metoda ini dipergunakan untuk jabatan
                                                    yang hasil kerjanya fisik atau bersifat kebendaan, atau hasil kerja non
                                                    fisik tetapi dapat dikuantifisir. Perlu diperhatikan, bahwa metoda ini
                                                    efektif dan mudah digunakan untuk jabatan yang hasil kerjanya hanya satu
                                                    jenis.
                                                </p>

                                                <p>
                                                    Dalam menggunakan metoda ini, informasi yang diperlukan adalah:
                                                </p>
                                                <ol>
                                                    <li class="level1">
                                                        <div class="li"> wujud hasil kerja dan satuannya;</div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> jumlah beban kerja yang tercemin dari target
                                                            hasil kerja yang harus dicapai;</div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> standar kemampuan rata-rata untuk memperoleh
                                                            hasil kerja.</div>
                                                    </li>
                                                </ol>

                                                <p>
                                                    Rumus menghitung dengan pendekatan metode ini adalah:
                                                </p>

                                                <p>
                                                    <img src="https://kms.krx-bkn.my.id/_media/ensiklopedia:pasted:20230220-061600.png" alt="" class="img-fluid">
                                                </p>
                                                <ul>
                                                    <li class="level1">
                                                        <div class="li"> <strong>Pendekatan Objek Kerja</strong></div>
                                                    </li>
                                                </ul>

                                                <p>
                                                    Objek kerja yang dimaksud disini adalah objek yang dilayani dalam
                                                    pelaksanaan pekerjaan. Metoda ini dipergunakan untuk jabatan yang beban
                                                    kerjanya bergantung dari jumlah objek yang harus dilayani.
                                                </p>

                                                <p>
                                                    Metode ini memerlukan informasi:
                                                </p>
                                                <ol>
                                                    <li class="level1">
                                                        <div class="li"> wujud objek kerja dan satuan;</div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> jumlah beban kerja yang tercemin dari
                                                            banyaknya objek yang harus dilayani;</div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> standar kemampuan rata-rata untuk melayani
                                                            objek kerja.</div>
                                                    </li>
                                                </ol>

                                                <p>
                                                    Rumus menghitung dengan pendekatan metoda ini adalah:
                                                </p>

                                                <p>
                                                    <img src="https://kms.krx-bkn.my.id/_media/ensiklopedia:pasted:20230220-061721.png" alt="" class="img-fluid">
                                                </p>
                                                <ul>
                                                    <li class="level1">
                                                        <div class="li"> <strong>Pendekatan Peralatan Kerja</strong>
                                                        </div>
                                                    </li>
                                                </ul>

                                                <p>
                                                    Peralatan kerja adalah peralatan yang digunakan dalam bekerja.
                                                    Metode ini digunakan untuk jabatan yang beban kerjanya bergantung pada
                                                    peralatan kerjanya.
                                                </p>

                                                <p>
                                                    Dalam menggunakan metoda ini, informasi yang diperlukan adalah:
                                                </p>
                                                <ol>
                                                    <li class="level1">
                                                        <div class="li"> satuan alat kerja</div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> jabatan yang diperlukan untuk pengoperasian
                                                            alat kerja;</div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> jumlah alat kerja yang dioperasikan;</div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> rasio jumlah pegawai per jabatan per alat
                                                            kerja (RPK);</div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> pergantian jam kerja (jaga)</div>
                                                    </li>
                                                </ol>

                                                <p>
                                                    Rumus perhitungannya adalah:
                                                </p>

