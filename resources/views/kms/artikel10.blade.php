                                            <h3 class="sectionedit5" id="penyusunan_analisis_jabatan_anjab">Penyusunan
                                                Analisis Jabatan (ANJAB)</h3>
                                            <div class="level3">

                                                <p>
                                                    <strong>Analisis Jabatan</strong> adalah proses pengumpulan, pencatatan,
                                                    pengolahan dan penyusunan data jabatan menjadi informasi jabatan.
                                                    Analisis Jabatan harus memuat informasi Jabatan sebagai berikut:
                                                </p>
                                                <ul>
                                                    <li class="level1 node">
                                                        <div class="li"> <strong>Identitas Jabatan</strong> merupakan
                                                            kumpulan informasi yang terdiri atas:</div>
                                                        <ul>
                                                            <li class="level2">
                                                                <div class="li"> nama Jabatan atau nomenklatur Jabatan,
                                                                    yaitu sebutan untuk memberi ciri dan gambaran atas isi
                                                                    jabatan, yang berupa sekelompok tugas yang melembaga
                                                                    atau menyatu dalam satu wadah jabatan, tugas, dan fungsi
                                                                    yang sama sebaiknya menggunakan nama jabatan yang sama.
                                                                </div>
                                                            </li>
                                                            <li class="level2">
                                                                <div class="li"> kode Jabatan, yaitu kode yang
                                                                    merepresentasikan suatu jabatan, yang dibuat untuk
                                                                    mempermudah inventarisasi jabatan.</div>
                                                            </li>
                                                            <li class="level2">
                                                                <div class="li"> unit kerja.</div>
                                                            </li>
                                                        </ul>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> <strong>Ikhtisar Jabatan</strong> adalah
                                                            ringkasan dari tugas-tugas yang dilakukan, yang tersusun dalam
                                                            satu kalimat yang mencerminkan pokok-pokok tugas jabatan.</div>
                                                    </li>
                                                    <li class="level1 node">
                                                        <div class="li"> <strong>Kualifikasi Jabatan</strong> adalah
                                                            persyaratan yang harus dipenuhi oleh pegawai untuk menduduki
                                                            suatu jabatan yang terdiri atas pendidikan, pelatihan dan
                                                            pengalaman, agar dapat melaksanakan tugas dengan baik.</div>
                                                        <ul>
                                                            <li class="level2">
                                                                <div class="li"> Pendidikan merupakan pendidikan formal
                                                                    minimal yang harus dimiliki Pemangku Jabatan untuk
                                                                    menduduki suatu jabatan disertai dengan jurusan yang
                                                                    sesuai dengan kebutuhan jabatan.</div>
                                                            </li>
                                                            <li class="level2">
                                                                <div class="li"> Pelatihan yang dimaksud terdiri atas:
                                                                    Pelatihan Kepemimpinan, Pelatihan Fungsional, dan
                                                                    Pelatihan Teknis.</div>
                                                            </li>
                                                            <li class="level2">
                                                                <div class="li"> Pengalaman merupakan pengalaman kerja
                                                                    Pemangku Jabatan pada bidang tertentu yang linier
                                                                    dan/atau berkaitan dengan tugas Jabatan.</div>
                                                            </li>
                                                        </ul>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> <strong>Tugas Pokok</strong> adalah paparan
                                                            semua tugas jabatan yang merupakan upaya pokok pemangku jabatan
                                                            dalam memproses bahan kerja menjadi hasil kerja menggunakan
                                                            perangkat kerja dalam kondisi tertentu.</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> <strong>Hasil Kerja</strong> adalah suatu
                                                            keluaran atau output dari pelaksanaan tugas jabatan yang dapat
                                                            diukur atau dihitung berdasarkan bukti fisik kegiatan.</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> <strong>Bahan Kerja</strong> adalah masukan
                                                            yang diproses dengan tindak kerja (tugas) menjadi hasil kerja.
                                                        </div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> <strong>Perangkat Kerja</strong> adalah acuan
                                                            atau pedoman yang digunakan untuk mengolah bahan kerja menjadi
                                                            hasil kerja.</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> <strong>Tanggung Jawab</strong> adalah
                                                            tuntutan Jabatan untuk menyelesaikan pekerjaan dengan sebaik
                                                            mungkin, tepat waktu, dan memberikan manfaat atas tugas yang
                                                            dilaksanakan serta berani menanggung risiko atas keputusan yang
                                                            diambil atau tindakan yang dilakukannya.</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> <strong>Wewenang</strong> adalah hak dan
                                                            kekuasaan pemangku jabatan untuk mengambil sikap atau menentukan
                                                            sikap pengambilan keputusan.</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> <strong>Korelasi Jabatan</strong> adalah
                                                            hubungan kerja antara Jabatan yang dianalisis dengan Jabatan
                                                            lainnya terkait dengan pelaksanaan tugas secara vertikal,
                                                            horizontal, dan/atau diagonal baik di dalam maupun di luar
                                                            Instansi Pemerintah dalam hal melaksanakan tugas pokok Jabatan.
                                                        </div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> <strong>Kondisi Lingkungan Kerja</strong>
                                                            merupakan keadaan tempat Pemangku Jabatan tersebut melaksanakan
                                                            tugas meliputi aspek lokasi kerja, suhu, udara, luas ruangan,
                                                            letak, penerangan, suara, keadaan tempat kerja, dan getaran.
                                                        </div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> <strong>Risiko Bahaya</strong> merupakan
                                                            potensi kejadian atau keadaan yang dapat membahayakan
                                                            keselamatan atau kesehatan Pemangku Jabatan secara fisik
                                                            dan/atau kejiwaan ketika melaksanakan tugas Jabatan.</div>
                                                    </li>
                                                    <li class="level1 node">
                                                        <div class="li"> <strong>Syarat Jabatan</strong> merupakan
                                                            syarat minimal yang harus dimiliki Pemangku Jabatan untuk
                                                            menduduki Jabatan yang terdiri atas:</div>
                                                        <ul>
                                                            <li class="level2">
                                                                <div class="li"> keterampilan; merupakan kemampuan dan
                                                                    penguasaan teknis operasional yang dibutuhkan Pemangku
                                                                    Jabatan agar tugas Jabatan dapat dilaksanakan dengan
                                                                    lebih cepat berdasarkan daftar keterampilan.</div>
                                                            </li>
                                                            <li class="level2">
                                                                <div class="li"> bakat kerja; merupakan kapasitas
                                                                    khusus atau kemampuan potensial yang dibutuhkan Pemangku
                                                                    Jabatan untuk dapat mempelajari dan memahami pekerjaan
                                                                    secara baik berdasarkan daftar bakat.</div>
                                                            </li>
                                                            <li class="level2">
                                                                <div class="li"> temperamen kerja; merupakan kemampuan
                                                                    menyesuaikan diri Pemangku Jabatan dengan sifat-sifat
                                                                    dominan pekerjaan berdasarkan daftar temperamen.</div>
                                                            </li>
                                                            <li class="level2">
                                                                <div class="li"> minat kerja; merupakan kecenderungan
                                                                    Pemangku Jabatan untuk memilih pekerjaan sesuai dengan
                                                                    kemauan, keinginan, dan/atau kemampuan berdasarkan
                                                                    daftar minat.</div>
                                                            </li>
                                                            <li class="level2">
                                                                <div class="li"> upaya fisik; merupakan penggunaan
                                                                    organ tubuh Pemangku Jabatan yang dominan dalam
                                                                    pelaksanaan tugas Jabatan berdasarkan daftar upaya
                                                                    fisik.</div>
                                                            </li>
                                                            <li class="level2">
                                                                <div class="li"> kondisi fisik; merupakan syarat
                                                                    keadaan fisik yang dibutuhkan Pemangku Jabatan untuk
                                                                    melaksanakan tugas Jabatan.</div>
                                                            </li>
                                                            <li class="level2">
                                                                <div class="li"> fungsi pekerja; merupakan tingkat
                                                                    kompleksitas tugas dalam kaitannya dengan data, orang,
                                                                    maupun benda berdasarkan daftar fungsi pekerja.</div>
                                                            </li>
                                                        </ul>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> <strong>Prestasi Kerja</strong> merupakan
                                                            prestasi Pemangku Jabatan yang diharapkan bernilai baik atau
                                                            sangat baik sesuai dengan ketentuan peraturan perundang-undangan
                                                            di bidang manajemen kinerja</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> <strong>Kelas Jabatan</strong> merupakan
                                                            kedudukan yang menunjukkan tingkat Jabatan dalam rangkaian
