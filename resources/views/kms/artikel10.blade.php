                                                </p>
                                                <ol>
                                                    <li class="level1">
                                                        <div class="li"> uraian tugas beserta jumlah beban untuk setiap
                                                            tugas;</div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> waktu penyelesaian tugas;</div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> jumlah waktu kerja efektif per hari rata-rata
