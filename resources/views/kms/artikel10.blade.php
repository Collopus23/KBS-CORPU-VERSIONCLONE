                                            <h4 id="tim_pelaksana_analisis_jabatan_dan_analisis_beban_kerja">Tim Pelaksana
                                                Analisis Jabatan dan Analisis Beban Kerja</h4>
                                            <div class="level4">

                                                <p>
                                                    <strong>Tugas</strong> Tim Pelaksana Analisis Jabatan dan Analisis Beban
                                                    Kerja adalah mengumpulkan data, menyusun informasi jabatan,
                                                    memverifikasi data, serta mengumpulkan beban kerja dalam jangka waktu
                                                    satu tahun.
                                                </p>

                                                <p>
                                                    <strong>Persyaratan</strong> untuk dapat diangkat menjadi anggota Tim
                                                    Pelaksana Analisis Jabatan dan Analisis Beban Kerja adalah:
                                                </p>
                                                <ul>
                                                    <li class="level1">
                                                        <div class="li"> <a href="/ensiklopedia:pns"
                                                                class="wikilink1" title="ensiklopedia:pns"
                                                                data-wiki-id="ensiklopedia:pns">PNS</a> dan/atau PPPK yang
                                                            telah mengikuti pelatihan dan/atau bimbingan teknis analisis
                                                            jabatan serta analisis beban kerja dan/atau;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> syarat objektif lain yang ditentukan oleh
                                                            pejabat yang berwenang, termasuk pengalaman dan kemampuan lain
                                                            yang diperlukan dalam pelaksanaan tugas tim.</div>
                                                    </li>
                                                </ul>

                                                <p>
                                                    <strong>Susunan keanggotaan</strong> Tim Pelaksana Analisis Jabatan dan
                                                    Analisis Beban Kerja terdiri atas:
                                                </p>
                                                <ul>
                                                    <li class="level1">
                                                        <div class="li"> Seorang Ketua merangkap anggota;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> Seorang Sekretaris merangkap anggota; dan
                                                        </div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> Paling sedikit 7 (tujuh) orang anggota,
                                                            termasuk ketua dan sekretaris.</div>
                                                    </li>
                                                </ul>

                                                <p>
                                                    Untuk menjamin objektivitas dalam pelaksanaan analisis jabatan dan
                                                    analisis beban kerja, kriteria jumlah Tim Pelaksana Analisis Jabatan dan
                                                    Analisis Beban Kerja anggota Tim Pelaksana Analisis Jabatan dan Analisis
                                                    Beban Kerja ditetapkan dalam jumlah ganjil.
                                                </p>

                                                <p>
                                                    <strong>Ketua</strong> Tim Pelaksana Analisis Jabatan dan Analisis Beban
                                                    Kerja dapat ditunjuk dari seorang pejabat JPT Pratama atau Administrator
                                                    yang secara fungsional bertanggung jawab di bidang organisasi dan/atau
                                                    kepegawaian.
                                                    Tugas Ketua Tim Pelaksana Analisis Jabatan dan Analisis Beban Kerja
                                                    adalah:
                                                </p>
                                                <ol>
                                                    <li class="level1">
                                                        <div class="li"> membuat rencana kerja pelaksanaan analisis
                                                            jabatan dan analisis beban kerja;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> memberikan pengarahan dan bimbingan kepada
                                                            anggota Tim Pelaksana Analisis Jabatan dan Analisis Beban Kerja;
                                                            dan</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> menyampaikan hasil pelaksanaan analisis
                                                            jabatan dan analisis beban kerja kepada Pejabat Pembina
                                                            Kepegawaian instansi yang bersangkutan.</div>
                                                    </li>
                                                </ol>

                                                <p>
                                                    <strong>Sekretaris</strong> Tim Pelaksana Analisis Jabatan dan Analisis
                                                    Beban Kerja dapat ditunjuk paling rendah seorang pejabat Pengawas yang
                                                    memiliki kemampuan dan pengalaman teknis di bidang analisis jabatan dan
                                                    analisis beban kerja atau Pejabat Fungsional yang membidangi analisis
                                                    jabatan dan analisis beban kerja.
                                                    Tugas Sekretaris Tim adalah:
                                                </p>
                                                <ol>
                                                    <li class="level1">
                                                        <div class="li"> membantu Ketua Tim dalam melaksanakan
                                                            tugasnya;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> Menyiapkan bahan diskusi, seminar atau
                                                            lokakarya;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> Menyelenggarakan diskusi, seminar atau
                                                            lokakarya; dan</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> Mempersiapkan segala sesuatu yang diperlukan
                                                            untuk kelancaran pelaksanaan analisis jabatan dan analisis beban
                                                            kerja, dan</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> Melaporkan hasil penyusunan analisis jabatan
