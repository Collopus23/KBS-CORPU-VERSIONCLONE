                                                    <img src="https://kms.krx-bkn.my.id/_media/ensiklopedia:pasted:20230220-062054.png" alt="" class="img-fluid">
                                                </p>
                                                <ul>
                                                    <li class="level1">
                                                        <div class="li"> <strong>Pendekatan Tugas per Tugas
                                                                Jabatan</strong></div>
                                                    </li>
                                                </ul>

                                                <p>
                                                    Metode ini adalah metode untuk menghitung kebutuhan pegawai pada jabatan
                                                    yang hasil kerjanya abstrak atau beragam. Hasil beragam artinya hasil
                                                    kerja dalam jabatan banyak jenisnya.
                                                </p>

                                                <p>
                                                    Informasi yang diperlukan untuk dapat menghitung dengan metoda ini
