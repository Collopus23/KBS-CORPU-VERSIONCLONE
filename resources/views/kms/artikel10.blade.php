                                            <h4 id="waktu_penyampaian_kebutuhan">Waktu Penyampaian Kebutuhan</h4>
                                            <div class="level4">

                                                <p>
                                                    Rincian hasil penyusunan kebutuhan jumlah dan jenis jabatan ASN setiap
                                                    tahun untuk penetapan kebutuhan ASN disampaikan kepada Menteri dan
                                                    Kepala BKN, dengan ketentuan sebagai berikut:
                                                </p>
                                                <ul>
                                                    <li class="level1">
                                                        <div class="li"> untuk Instansi Pusat disampaikan oleh PPK
                                                            Instansi Pusat paling lambat akhir bulan Maret untuk penetapan
                                                            kebutuhan ASN tahun berikutnya;</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> untuk Instansi Daerah Provinsi disampaikan
                                                            oleh Gubernur paling lambat akhir bulan Maret untuk penetapan
                                                            kebutuhan ASN tahun berikutnya; dan</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> untuk Instansi Daerah Kabupaten atau Kota
                                                            disampaikan oleh Bupati atau Walikota paling lambat akhir bulan
                                                            Maret untuk penetapan kebutuhan ASN tahun berikutnya, setelah
                                                            berkoordinasi dengan Gubernur.</div>
                                                    </li>
                                                </ul>

