                                            <h2 class="sectionedit3" id="pendahuluan">Pendahuluan</h2>
                                            <div class="level2">

                                                <p>
                                                    Aparatur Sipil Negara yang selanjutnya disingkat ASN adalah profesi bagi
                                                    Pegawai Negeri Sipil (<a href="/ensiklopedia:pns" class="wikilink1"
                                                        title="ensiklopedia:pns" data-wiki-id="ensiklopedia:pns">PNS</a>)
                                                    dan Pegawai Pemerintah dengan Perjanjian Kerja (PPPK) yang bekerja pada
                                                    instansi pemerintah. <a href="/ensiklopedia:pns" class="wikilink1"
                                                        title="ensiklopedia:pns" data-wiki-id="ensiklopedia:pns">PNS</a> dan
                                                    PPPK diangkat oleh pejabat pembina kepegawaian dan diserahi tugas dalam
                                                    suatu jabatan pemerintahan atau diserahi tugas negara lainnya dan digaji
                                                    berdasarkan peraturan perundang-undangan.
                                                </p>

                                                <p>
                                                    Berdasarkan <a href="/ensiklopedia:uu05-2014" class="wikilink1"
                                                        title="ensiklopedia:uu05-2014"
                                                        data-wiki-id="ensiklopedia:uu05-2014">UU Nomor 5 Tahun 2014</a>
                                                    tentang ASN disebutkan bahwa setiap instansi wajib menyusun kebutuhan
                                                    jumlah dan jenis jabatan <a href="/ensiklopedia:pns" class="wikilink1"
                                                        title="ensiklopedia:pns" data-wiki-id="ensiklopedia:pns">PNS</a>
                                                    berdasarkan analisis jabatan dan analisis beban kerja yang dilakukan
                                                    untuk jangka waktu 5 (lima) tahun yang diperinci per 1 (satu) tahun
                                                    berdasarkan prioritas kebutuhan. Setiap Instansi Pemerintah juga wajib
                                                    menyusun kebutuhan jumlah dan jenis jabatan PPPK berdasarkan analisis
                                                    jabatan dan analisis beban kerja. Dalam PP No 49 Tahun 2018 tentang
                                                    Manajemen PPPK ditentukan bahwa penyusunan kebutuhan jumlah PPPK
                                                    merupakan satu kesatuan dengan penyusunan kebutuhan <a
                                                        href="/ensiklopedia:pns" class="wikilink1" title="ensiklopedia:pns"
                                                        data-wiki-id="ensiklopedia:pns">PNS</a>.
                                                </p>

                                                <p>
                                                    Terkait dengan penyusunan kebutuhan <a href="/ensiklopedia:pns"
                                                        class="wikilink1" title="ensiklopedia:pns"
                                                        data-wiki-id="ensiklopedia:pns">PNS</a>, Menteri Pendayagunaan
                                                    Aparatur Negara dan Reformasi Birokrasi menetapkan kebutuhan jumlah dan
                                                    jabatan <a href="/ensiklopedia:pns" class="wikilink1"
                                                        title="ensiklopedia:pns" data-wiki-id="ensiklopedia:pns">PNS</a>
                                                    secara nasional dengan memperhatikan pendapat menteri yang
                                                    menyelenggarakan urusan pemerintahan di bidang keuangan dan pertimbangan
                                                    teknis dari kepala BKN.
                                                </p>

                                                <p>
                                                    Berdasarkan pertimbangan teknis Kepala BKN, Menteri Pendayagunaan
                                                    Aparatur Negara dan Reformasi Birokrasi menyusun rencana pemenuhan
                                                    kebutuhan <a href="/ensiklopedia:pns" class="wikilink1"
                                                        title="ensiklopedia:pns" data-wiki-id="ensiklopedia:pns">PNS</a>
                                                    berdasarkan prioritas pembangunan nasional. Rencana pemenuhan kebutuhan
                                                    <a href="/ensiklopedia:pns" class="wikilink1"
                                                        title="ensiklopedia:pns" data-wiki-id="ensiklopedia:pns">PNS</a>
                                                    disampaikan oleh Menteri PAN RB kepada menteri yang menyelenggarakan
                                                    urusan pemerintahan di bidang keuangan untuk dimintakan pendapat paling
                                                    lambat akhir bulan April untuk rencana pemenuhan kebutuhan <a
                                                        href="/ensiklopedia:pns" class="wikilink1"
                                                        title="ensiklopedia:pns" data-wiki-id="ensiklopedia:pns">PNS</a>
                                                    tahun berikutnya.
