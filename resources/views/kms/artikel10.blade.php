                                            <h3 class="sectionedit7" id="penyusunan_peta_jabatan">Penyusunan Peta Jabatan
                                            </h3>
                                            <div class="level3">

                                                <p>
                                                    Peta Jabatan merupakan susunan nama dan tingkat JPT, JA, dan JF yang
                                                    tergambar dalam struktur unit organisasi dari tingkat yang paling rendah
                                                    sampai dengan yang paling tinggi terdiri atas:
                                                </p>
                                                <ol>
                                                    <li class="level1 node">
                                                        <div class="li"> <strong>Struktur Jabatan</strong>. Struktur
                                                            Jabatan merupakan tabel yang berisi Jabatan pada Instansi
                                                            Pemerintah yang disusun sebagai berikut:</div>
                                                        <ul>
                                                            <li class="level2">
                                                                <div class="li"> dibuat per unit organisasi;</div>
                                                            </li>
                                                            <li class="level2">
                                                                <div class="li"> dimulai dari jenjang Jabatan paling
                                                                    tinggi sampai dengan paling rendah; dan</div>
                                                            </li>
                                                            <li class="level2">
                                                                <div class="li"> nama setiap Jabatan yang dibutuhkan,
                                                                    baik JPT, JA, dan/atau JF.</div>
                                                            </li>
                                                        </ul>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> <strong>Beban Kerja Unit Organisasi</strong>.
                                                            Beban kerja unit organisasi merupakan jumlah target pekerjaan
                                                            yang harus diselesaikan oleh satuan unit organisasi Instansi
