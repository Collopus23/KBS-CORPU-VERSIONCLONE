                                            <h2 class="sectionedit3" id="definisi">Definisi</h2>
                                            <div class="level2">

                                                <p>
                                                    Presiden Republik Indonesia telah meluncurkan <em>core values</em>
                                                    (nilai-nilai dasar) ASN BerAKHLAK dan <em>employer branding</em> ASN
                                                    yaitu “Bangga Melayani Bangsa”. Nilai-nilai dasar BerAKHLAK menjadi
                                                    dasar penguatan budaya kerja di instansi pemerintah untuk mendukung
                                                    pencapaian kinerja individu dan tujuan organisasi/instansi.
                                                </p>

                                                <p>
                                                    Core value ASN BerAKHLAK merupakan akronim dari:
                                                </p>
                                                <ol>
                                                    <li class="level1">
                                                        <div class="li"> Berorientasi Pelayanan, yaitu komitmen
                                                            memberikan pelayanan prima demi kepuasan masyarakat.</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> Akuntabel, yaitu bertanggungjawab atas
                                                            kepercayaan yang diberikan.</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> Kompeten, yaitu terus belajar dan mengembangkan
                                                            kapabilitas.</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> Harmonis, yaitu saling peduli dan menghargai
                                                            perbedaan.</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> Loyal, yaitu berdedikasi dan mengutamakan
                                                            kepentingan Bangsa dan Negara.</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> Adaptif, yaitu terus berinovasi dan antusias
                                                            dalam menggerakkan serta menghadapi perubahan.</div>
                                                    </li>
                                                    <li class="level1">
                                                        <div class="li"> Kolaboratif, yaitu membangun kerja sama yang
                                                            sinergis.</div>
                                                    </li>
                                                </ol>

                                            </div>

                                            <h2 class="sectionedit4"
                                                id="panduan_perilaku_kode_etik_sesuai_dengan_permen_pan_rb_no_6_tahun_2022">
                                                Panduan Perilaku (Kode Etik) sesuai dengan Permen PAN RB No. 6 Tahun 2022
                                            </h2>
                                            <div class="level2">

                                            </div>

                                            <h4 id="berorientasi_pelayanan">- Berorientasi Pelayanan</h4>
                                            <div class="level4">
                                                <ul>
                                                    <li class="level1">
                                                        <div class="li"> Memahami dan memenuhi kebutuhan masyarakat
                                                        </div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> Ramah, cekatan, solutif, dan dapat diandalkan
                                                        </div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> Melakukan perbaikan tiada henti</div>
                                                    </li>
                                                </ul>

                                            </div>

                                            <h4 id="akuntabel">- Akuntabel</h4>
                                            <div class="level4">
                                                <ul>
                                                    <li class="level1">
                                                        <div class="li"> Melaksanakan tugas dengan jujur, bertanggung
                                                            jawab, cermat, disiplin dan berintegritas tinggi;</div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> Menggunakan kekayaan dan barang milik negara
                                                            secara bertanggung jawab, efektif, dan efisien;</div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> Tidak menyalahgunakan kewenangan jabatan.</div>
                                                    </li>
                                                </ul>

                                            </div>

                                            <h4 id="kompeten">- Kompeten</h4>
                                            <div class="level4">
                                                <ul>
                                                    <li class="level1">
                                                        <div class="li"> Meningkatkan kompetensi diri untuk menjawab
                                                            tantangan yang selalu berubah;</div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> Membantu orang lain belajar;</div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> Melaksanakan tugas dengan kualitas terbaik.
                                                        </div>
                                                    </li>
                                                </ul>

                                            </div>

                                            <h4 id="harmonis">- Harmonis</h4>
                                            <div class="level4">
                                                <ul>
                                                    <li class="level1">
                                                        <div class="li"> Menghargai setiap orang apapun latar
                                                            belakangnya;</div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> Suka menolong orang lain; </div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> Membangun lingkungan kerja yang kondusif.
                                                        </div>
                                                    </li>
                                                </ul>

                                            </div>

                                            <h4 id="loyal">- Loyal</h4>
                                            <div class="level4">
                                                <ul>
                                                    <li class="level1">
                                                        <div class="li"> Memegang teguh ideologi Pancasila,
                                                            Undang-Undang Dasar Negara Republik Indonesia tahun 1945, setia
                                                            kepada Negara Kesatuan Republik Indonesia serta pemerintahan
                                                            yang sah;</div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> Menjaga nama baik sesama ASN, pimpinan,
                                                            instansi, dan negara;</div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> Menjaga rahasia jabatan dan negara.</div>
                                                    </li>
                                                </ul>

                                            </div>

                                            <h4 id="adaptif">- Adaptif</h4>
                                            <div class="level4">
                                                <ul>
                                                    <li class="level1">
                                                        <div class="li"> Cepat menyesuaikan diri menghadapi perubahan;
                                                        </div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> Terus berinovasi dan mengembangkan
                                                            kreativitas;</div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> Bertindak proaktif.</div>
                                                    </li>
                                                </ul>

                                            </div>

                                            <h4 id="kolaboratif">- Kolaboratif</h4>
                                            <div class="level4">
                                                <ul>
                                                    <li class="level1">
                                                        <div class="li"> Memberi kesempatan kepada berbagai pihak untuk
                                                            berkontribusi;</div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> Terbuka dalam bekerja sama untuk menghasilkan
                                                            nilai tambah;</div>
                                                    </li>
                                                    <li class="level2">
                                                        <div class="li"> Menggerakkan pemanfaatan berbagai sumberdaya
                                                            untuk tujuan bersama.</div>
                                                    </li>
                                                </ul>

